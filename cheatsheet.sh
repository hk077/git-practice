echo git utilities:
echo git init - initialises folder as git repo
echo git add [file or folder] - stages changes to local repo
echo git commit -m "[message]" - commits stage to branch with message
echo git push - pushes commit to remote repo
echo git pull - pulls from remote branch to local
echo git clone - clones repo from remote location to folder
echo git switch [branch] - moves to specified branch
echo git branch - lists branches in repo
echo git checkout -b [branchName] - creates new branch on local repo
echo git push -u origin [branchName] - pushes branch from local to remote
echo git pull origin [branchName] - pulls data from remote branch into current local branch

